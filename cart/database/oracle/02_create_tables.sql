CREATE TABLE USER_ACCOUNTS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(180) NOT NULL,
    ROLE VARCHAR2(40) NOT NULL,
    ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT CK_USER_ACCOUNTS_ACTIVE CHECK (ACTIVE IN (0, 1)),
    CONSTRAINT UK_USER_ACCOUNTS_USERNAME UNIQUE (USERNAME),
    CONSTRAINT UK_USER_ACCOUNTS_EMAIL UNIQUE (EMAIL)
);

CREATE TABLE PRODUCTS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(150) NOT NULL,
    CATEGORY VARCHAR2(80) NOT NULL,
    PRICE NUMBER(12,2) NOT NULL,
    ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT CK_PRODUCTS_ACTIVE CHECK (ACTIVE IN (0, 1))
);

CREATE TABLE INVENTORY_ITEMS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER(19) NOT NULL,
    QUANTITY NUMBER(10) NOT NULL,
    WAREHOUSE_CODE VARCHAR2(40) NOT NULL
);

CREATE TABLE CART_ITEMS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER(19) NOT NULL,
    PRODUCT_ID NUMBER(19) NOT NULL,
    QUANTITY NUMBER(10) NOT NULL
);

CREATE TABLE SALES_ORDERS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER(19) NOT NULL,
    STATUS VARCHAR2(40) NOT NULL,
    TOTAL NUMBER(12,2) NOT NULL
);

CREATE TABLE PAYMENT_TRANSACTIONS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID NUMBER(19) NOT NULL,
    AMOUNT NUMBER(12,2) NOT NULL,
    STATUS VARCHAR2(40) NOT NULL
);

CREATE TABLE SHIPMENTS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID NUMBER(19) NOT NULL,
    CARRIER VARCHAR2(80) NOT NULL,
    STATUS VARCHAR2(40) NOT NULL
);

CREATE TABLE REVIEWS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER(19) NOT NULL,
    RATING NUMBER(2) NOT NULL,
    COMMENT_TEXT VARCHAR2(1000)
);

CREATE TABLE CAMPAIGNS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE VARCHAR2(50) NOT NULL,
    DISCOUNT_PERCENT NUMBER(3) NOT NULL,
    ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT CK_CAMPAIGNS_ACTIVE CHECK (ACTIVE IN (0, 1)),
    CONSTRAINT CK_CAMPAIGNS_DISCOUNT CHECK (DISCOUNT_PERCENT BETWEEN 0 AND 100),
    CONSTRAINT UK_CAMPAIGNS_CODE UNIQUE (CODE)
);

CREATE INDEX IDX_INVENTORY_PRODUCT_ID ON INVENTORY_ITEMS (PRODUCT_ID);
CREATE INDEX IDX_CART_USER_ID ON CART_ITEMS (USER_ID);
CREATE INDEX IDX_CART_PRODUCT_ID ON CART_ITEMS (PRODUCT_ID);
CREATE INDEX IDX_ORDERS_USER_ID ON SALES_ORDERS (USER_ID);
CREATE INDEX IDX_PAYMENT_ORDER_ID ON PAYMENT_TRANSACTIONS (ORDER_ID);
CREATE INDEX IDX_SHIPMENT_ORDER_ID ON SHIPMENTS (ORDER_ID);
CREATE INDEX IDX_REVIEWS_PRODUCT_ID ON REVIEWS (PRODUCT_ID);
